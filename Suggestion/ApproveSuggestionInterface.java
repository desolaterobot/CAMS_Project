package Suggestion;

/**
 * The ApproveSuggestionInterface provides methods for viewing camp suggestions and approving a suggestion.
 */
public interface ApproveSuggestionInterface{
	/**
     * Displays camp suggestions for viewing.
     */
	public void viewCampSuggestions();

	/**
     * Approves a camp suggestion.
     */
	public void approveSuggestion();
}

package Enquiry;
/**
 * The EnquiryReplyInterface provides methods for viewing camp enquiries and replying to an enquiry.
 */
public interface EnquiryReplyInterface{
	/**
     * Displays camp enquiries for viewing.
     */
	public void viewCampEnquiries();
	
	/**
     * Allows a user to reply to a specific enquiry.
     */
	public void replyToEnquiry();
}
